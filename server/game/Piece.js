const PIECE_TYPES = ["I", "O", "T", "S", "Z", "J", "L"];

function Piece(type = null) {
  this.type =
    type || PIECE_TYPES[Math.floor(Math.random() * PIECE_TYPES.length)];
  this.rotation = 0;
  this.x = 3; // Starting column
  this.y = 0; // Starting row
}

Piece.prototype.getShape = function () {
  return SHAPES[this.type][this.rotation];
};

Piece.prototype.rotate = function () {
  this.rotation = (this.rotation + 1) % 4;
};

Piece.prototype.moveLeft = function () {
  this.x -= 1;
};

Piece.prototype.moveRight = function () {
  this.x += 1;
};

Piece.prototype.moveDown = function () {
  this.y += 1;
};

Piece.prototype.clone = function () {
  const cloned = new Piece(this.type);
  cloned.rotation = this.rotation;
  cloned.x = this.x;
  cloned.y = this.y;
  return cloned;
};

Piece.prototype.serialize = function () {
  return {
    type: this.type,
    rotation: this.rotation,
    x: this.x,
    y: this.y,
  };
};

Piece.createFromType = function (type) {
  return new Piece(type);
};

module.exports = Piece;
module.exports.PIECE_TYPES = PIECE_TYPES;
module.exports.SHAPES = SHAPES;
