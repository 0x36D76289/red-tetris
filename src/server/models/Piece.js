const SHAPE_NAMES = Object.keys(SHAPES);

class Piece {
  constructor(type = null, x = 3, y = 0, rotation = 0) {
    this.type =
      type || SHAPE_NAMES[Math.floor(Math.random() * SHAPE_NAMES.length)];
    this.x = x;
    this.y = y;
    this.rotation = rotation;
    this.color = SHAPES[this.type].color;
  }

  getShape() {
    return SHAPES[this.type].rotations[this.rotation];
  }

  clone() {
    return new Piece(this.type, this.x, this.y, this.rotation);
  }

  rotate(direction = 1) {
    this.rotation = (this.rotation + direction + 4) % 4;
    return this;
  }

  moveLeft() {
    this.x -= 1;
    return this;
  }

  moveRight() {
    this.x += 1;
    return this;
  }

  moveDown() {
    this.y += 1;
    return this;
  }

  toJSON() {
    return {
      type: this.type,
      x: this.x,
      y: this.y,
      rotation: this.rotation,
      color: this.color,
    };
  }

  static fromJSON(data) {
    return new Piece(data.type, data.x, data.y, data.rotation);
  }

  static getRandomType() {
    return SHAPE_NAMES[Math.floor(Math.random() * SHAPE_NAMES.length)];
  }

  static getShapeData(type) {
    return SHAPES[type];
  }
}

export { Piece, SHAPES, SHAPE_NAMES };
